// Initialize database
db.serialize(() => {
  // Drop existing tables
  db.run("DROP TABLE IF EXISTS products");
  db.run("DROP TABLE IF EXISTS users");
  db.run("DROP TABLE IF EXISTS orders");
  db.run("DROP TABLE IF EXISTS activity_log");

  // Create tables with updated schema
  db.run(`
    CREATE TABLE users (
      id INTEGER PRIMARY KEY AUTOINCREMENT,
      email TEXT NOT NULL UNIQUE,
      password TEXT NOT NULL,
      name TEXT NOT NULL,
      role TEXT NOT NULL
    )
  `);

  db.run(`
    CREATE TABLE products (
      id INTEGER PRIMARY KEY AUTOINCREMENT,
      name TEXT NOT NULL,
      animal TEXT NOT NULL,
      category TEXT NOT NULL,
      price INTEGER NOT NULL,
      image TEXT NOT NULL,
      stock INTEGER NOT NULL,
      rating REAL NOT NULL
    )
  `);

  db.run(`
    CREATE TABLE orders (
      id INTEGER PRIMARY KEY AUTOINCREMENT,
      customerId INTEGER NOT NULL,
      customerName TEXT NOT NULL,
      total INTEGER NOT NULL,
      status TEXT NOT NULL,
      date TEXT NOT NULL,
      FOREIGN KEY (customerId) REFERENCES users (id)
    )
  `);

  db.run(`
    CREATE TABLE activity_log (
      id INTEGER PRIMARY KEY AUTOINCREMENT,
      userId INTEGER,
      action TEXT NOT NULL,
      timestamp DATETIME DEFAULT CURRENT_TIMESTAMP,
      FOREIGN KEY (userId) REFERENCES users (id)
    )
  `);

  // Insert users
  const userStmt = db.prepare(
    "INSERT INTO users (email, password, name, role) VALUES (?, ?, ?, ?)"
  );
  users.forEach((user) => {
    userStmt.run(user.email, user.password, user.name, user.role);
  });
  userStmt.finalize();

  // Insert products
  const productStmt = db.prepare(
    "INSERT INTO products (name, animal, category, price, image, stock, rating) VALUES (?, ?, ?, ?, ?, ?, ?)"
  );
  products.forEach((product) => {
    productStmt.run(
      product.name,
      product.animal,
      product.category,
      product.price,
      product.image,
      product.stock,
      product.rating
    );
  });
  productStmt.finalize();

  console.log(`âœ… Database seeded successfully!`);
  console.log(`   - ${users.length} users created`);
  console.log(`   - ${products.length} products created`);
  console.log(
    `   - Dogs: ${products.filter((p) => p.animal === "Dogs").length} products`
  );
  console.log(
    `   - Cats: ${products.filter((p) => p.animal === "Cats").length} products`
  );
  console.log(
    `   - Birds: ${
      products.filter((p) => p.animal === "Birds").length
    } products`
  );

  db.close((err) => {
    if (err) {
      console.error("Error closing database:", err.message);
    } else {
      console.log("Database connection closed.");
    }
    process.exit(0);
  });
});
